# Замінюємо блок після st.button("✨ Сформувати пакет документів")

if st.button("✨ Сформувати пакет документів"):
    if protocol:
        medical_files = upload_medical_bases()
        
        if len(medical_files) == 2:
            try:
                model = genai.GenerativeModel('gemini-3-flash-preview') # або 1.5 flash для швидкості
                
                # ЕТАП 1: Тільки Протокол + АКМІ
                with st.spinner("Крок 1: Пошук кодів в АКМІ..."):
                    akmi_prompt = f"""
                    Аналізуй цей ПРОТОКОЛ ОПЕРАЦІЇ. 
                    Використовуй ТІЛЬКИ файл АКМІ (nk-026_2021_.pdf).
                    
                    ЗАВДАННЯ:
                    1. Визнач усі коди процедур АКМІ, які відповідають протоколу.
                    2. Якщо це розшарування аорти (I71.0) або FET — виділи окремо коди для відкритої та ендоваскулярної частин.
                    3. Надай відповідь у вигляді простого списку кодів (наприклад: 38550-00, 38553-00).
                    
                    ПРОТОКОЛ:
                    {protocol}
                    """
                    # Передаємо ТІЛЬКИ перший файл (АКМІ)
                    akmi_response = model.generate_content([medical_files[0], akmi_prompt])
                    extracted_codes = akmi_response.text
                    st.success("✅ Коди АКМІ знайдено")

                # ЕТАП 2: Тільки Знайдені коди + ДСГ (Наказ 798)
                with st.spinner("Крок 2: Пошук у ДСГ та фінальне групування..."):
                    dsg_prompt = f"""
                    Ти — медичний аудитор. Маєш список кодів АКМІ: {extracted_codes}.
                    Використовуй файл Наказу №798 (36897-dn_798...).
                    
                    ЗАВДАННЯ:
                    1. Знайди ці коди у таблицях Наказу. Зосередься лише на сторінках, де згадуються ці коди.
                    2. Визнач номер ДСГ та її назву для кожного коду.
                    3. Обери ОСНОВНУ ДСГ (найдорожчу/найскладнішу) згідно з правилами групування.
                    4. Сформуй фінальний звіт.
                    
                    ФОРМАТ ЗВІТУ:
                    - Таблиця: Код АКМІ | Назва | ДСГ | Коефіцієнт (якщо є)
                    - Рекомендований діагноз МКХ-10
                    - Короткий висновок для аудиту
                    """
                    # Передаємо ТІЛЬКИ другий файл (ДСГ) + результат першого кроку
                    final_response = model.generate_content([medical_files[1], dsg_prompt])

                # Виведення результатів
                with col2:
                    st.subheader("Результат аналізу")
                    tab1, tab2 = st.tabs(["📊 Фінальний звіт", "⚙️ Проміжні коди"])
                    with tab1:
                        st.markdown(final_response.text)
                    with tab2:
                        st.info("Коди, знайдені на першому етапі:")
                        st.code(extracted_codes)

            except Exception as e:
                st.error(f"Помилка: {e}")
